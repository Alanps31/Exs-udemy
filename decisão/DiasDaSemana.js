function imprimeDia(num) {
    if(num === 1) {
        console.log('1 - Domingo')
    }else if (num === 2) {
        console.log("2 - segunda")
    }else if (num === 3) {
        console.log("3 - Terça")
    }else if (num === 4) {
        console.log("4 - Quarta")
    }else if (num === 5) {
        console.log("5 - Quinta")
    }else if (num === 6) {
        console.log("6 - Sexta")
    }else if (num === 7) {
        console.log("7 - Sábado")
    }else{
        console.log("Valor invalido")
    }

}

imprimeDia('1')